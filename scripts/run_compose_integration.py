def run_index_golden_via_mcp(mcp_url: str = MCP_URL) -> tuple[bool, str]:
    script = REPO_ROOT / "scripts" / "reindex_graphrag.py"
    if not script.is_file():
        return False, f"missing {script}"
    env = _host_test_env()
