def compose_cmd(*args: str, external_tei: bool = False, aspire_stack: bool = False) -> list[str]:
    env = dict(os.environ)
    if ENV_FILE.is_file():
        for line in ENV_FILE.read_text(encoding="utf-8").splitlines():
            line = line.strip()
            if not line or line.startswith("#") or "=" not in line:
                continue
            key, value = line.split("=", 1)
            env[key] = value
    return [
        *COMPOSE_BASE,
        *compose_file_args(env=env, include_tei=not external_tei),
        *args,
    ]


def _compose_profile_args(*, external_tei: bool) -> list[str]:
    return [] if external_tei else ["--profile", COMPOSE_PROFILE]


def _capture_failure_logs(*, external_tei: bool = False) -> str:
    """Best-effort container logs to diagnose a failed ``up --wait`` (e.g. unhealthy tei)."""
    logs = _run(
        compose_cmd(
            *_compose_profile_args(external_tei=external_tei),
            "logs",
            "--no-color",
            "--tail",
            "200",
            external_tei=external_tei,
        )
    )
    out = (logs.stdout or "") + (logs.stderr or "")
    return out.strip()[-6000:]


def preflight_host_tei(*, tei_url: str = HOST_TEI_URL) -> tuple[bool, str]:
    """Verify host-native TEI is reachable before compose up (ADR 0029)."""
    ok, detail = http_ok(f"{tei_url.rstrip('/')}/health")
    if not ok:
        return False, f"host TEI not healthy at {tei_url}: {detail}"
    return True, detail


def check_tei_container_absent(
    *,
    tei_container: str = TEI_CONTAINER,
    run_cmd: Callable[..., subprocess.CompletedProcess] | None = None,
) -> tuple[bool, str]:
    """Confirm bundled ``tei`` service was not started (external TEI path)."""
    runner = run_cmd or _run
    proc = runner(
        ["docker", "ps", "--filter", f"name={tei_container}", "--format", "{{.Names}}"]
    )
    if proc.returncode != 0:
        detail = (proc.stderr or proc.stdout or "docker ps failed").strip()
        return False, detail[-2000:]
    names = (proc.stdout or "").strip()
    if names:
        return False, f"container present: {names}"
    return True, "no bundled tei container"


def deploy(
    *,
    services: tuple[str, ...] = SERVICES,
    external_tei: bool = False,
) -> tuple[bool, str]:
    build = _run(compose_cmd("build", "mcp_server", external_tei=external_tei))
    if build.returncode != 0:
        return False, build.stderr or build.stdout or "compose build failed"
    up = _run(
        compose_cmd(
            *_compose_profile_args(external_tei=external_tei),
            "up",
            "-d",
            "--wait",
            *services,
            external_tei=external_tei,
        )
    )
    if up.returncode != 0:
        detail = up.stderr or up.stdout or "compose up failed"
        container_logs = _capture_failure_logs(external_tei=external_tei)
        if container_logs:
            detail = f"{detail}\n--- container logs (tail) ---\n{container_logs}"
        return False, detail
    return True, "deployed"


def http_ok(url: str, timeout: float = 3.0) -> tuple[bool, str]:
    try:
        with urllib.request.urlopen(url, timeout=timeout) as resp:
            body = resp.read(500).decode("utf-8", errors="replace")
            return True, body[:200]
    except urllib.error.HTTPError as exc:
        return False, f"HTTP {exc.code}"
    except Exception as exc:
        return False, str(exc)


def wait_http(url: str, *, attempts: int = 30, interval: float = 2.0) -> tuple[bool, str]:
    last = ""
    for _ in range(attempts):
        ok, msg = http_ok(url)
        if ok:
            return True, msg
        last = msg
        time.sleep(interval)
    return False, last


