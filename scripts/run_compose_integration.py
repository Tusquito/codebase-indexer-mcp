def check_tei_gpu_visible(
    *,
    tei_container: str = TEI_CONTAINER,
    tei_url: str = TEI_URL,
    model: str = JINA_DENSE_MODEL,
    vector_size: int = DENSE_VECTOR_SIZE,
    run_cmd: Callable[..., subprocess.CompletedProcess] | None = None,
    embed_fn: Callable[[str, str, int], tuple[bool, str]] | None = None,
) -> tuple[bool, str]:
    """Embed smoke + nvidia-smi in TEI container (ADR 0025)."""
    runner = run_cmd or _run
    embed = embed_fn or _tei_embed

    ok, detail = embed(tei_url, model, vector_size)
    if not ok:
        return False, f"embed failed: {detail}"

    smi = runner(["docker", "exec", tei_container, "nvidia-smi"])
    if smi.returncode != 0:
        detail = (smi.stderr or smi.stdout or "nvidia-smi failed").strip()
        return False, detail[-2000:]

    smi_out = (smi.stdout or "") + (smi.stderr or "")
    return True, smi_out.strip()[-2000:]


def _tei_embed(tei_url: str, model: str, vector_size: int) -> tuple[bool, str]:
    payload = json.dumps(
        {"model": model, "input": "gpu integration probe"}
    ).encode()
    req = urllib.request.Request(
        f"{tei_url.rstrip('/')}/v1/embeddings",
        data=payload,
        headers={"Content-Type": "application/json"},
        method="POST",
    )
    try:
        with urllib.request.urlopen(req, timeout=120) as resp:
            body = json.loads(resp.read().decode("utf-8"))
            items = body.get("data") or []
            if not items:
                return False, "no embedding data"
            dim = len(items[0].get("embedding") or [])
            if dim != vector_size:
                return False, f"dimension {dim}, expected {vector_size}"
            return True, f"dim={dim}"
    except urllib.error.HTTPError as exc:
        err_body = exc.read(500).decode("utf-8", errors="replace")
        return False, f"HTTP {exc.code}: {err_body}"
    except Exception as exc:
        return False, str(exc)


