def write_integration_env(
    workspace_root: Path,
    *,
    graph: bool = False,
    external_tei: bool = False,
    quality_rerank: bool = False,
) -> None:
    """Minimal compose env for integration — does not touch developer .env."""
    workspace = workspace_root.resolve()
    aspire_cache = ASPIRE_FASTEMBED_CACHE.resolve()
    accelerator = "cpu" if external_tei else get_accelerator()
    env_for_defaults = {"ACCELERATOR": accelerator}
    tei_image_line = ""
    mkl_line = ""
    if accelerator == "cpu" and not external_tei:
        tei_image = tei_image_default(env_for_defaults)
        tei_image_line = f"TEI_IMAGE={tei_image}\n"
        if container_arch() == "amd64" and "arm64" not in tei_image.lower():
            mkl_line = "TEI_MKL_INSTRUCTIONS=AVX2\n"

