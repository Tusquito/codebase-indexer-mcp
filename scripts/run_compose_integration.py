def aspire_compose_cmd(
    *args: str,
    graph: bool = False,
    gpu_colbert: bool = False,
    external_tei: bool = False,
) -> list[str]:
    env = dict(os.environ)
    if ENV_FILE.is_file():
        for line in ENV_FILE.read_text(encoding="utf-8").splitlines():
            line = line.strip()
            if not line or line.startswith("#") or "=" not in line:
                continue
            key, value = line.split("=", 1)
            env[key] = value
    file_args = aspire_compose_file_args(
        repo_root=REPO_ROOT,
        env=env,
        include_tei=not external_tei,
        include_neo4j=graph,
        gpu_colbert=gpu_colbert,
    )
    return ["docker", "compose", "--env-file", str(ENV_FILE), *file_args, *args]


def seed_aspire_sparse_cache() -> tuple[bool, str]:
    """Download Qdrant/bm25 into ASPIRE_FASTEMBED_CACHE for the .NET OnnxSparseEmbedder."""
    ASPIRE_FASTEMBED_CACHE.mkdir(parents=True, exist_ok=True)
    cache = str(ASPIRE_FASTEMBED_CACHE.resolve())
    os.environ["ASPIRE_FASTEMBED_CACHE"] = cache
    proc = _run(
        [
            "uv",
            "run",
            "python",
            "-c",
            (
                "from fastembed.sparse import SparseTextEmbedding; "
                f"SparseTextEmbedding(model_name='Qdrant/bm25', cache_dir=r'{cache}')"
            ),
        ],
        cwd=BENCHMARKS,
    )
    detail = ((proc.stdout or "") + (proc.stderr or "")).strip()[-1500:]
    if proc.returncode != 0:
        return False, detail or "sparse model seed failed"
    return True, f"seeded BM25 at {cache}"


