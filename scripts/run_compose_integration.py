def run_quality_validation(
    *,
    compare: Path,
    threshold: float,
    rerank: bool,
    mcp_url: str = MCP_URL,
) -> dict[str, dict[str, str]]:
    rows: dict[str, dict[str, str]] = {}

    ok, detail = run_validate_labels()
    rows["validate_labels"] = {
        "status": "pass" if ok else "fail",
        "detail": detail,
    }
    if not ok:
        indexed, index_detail = run_index_golden_via_mcp(mcp_url)
        rows["index_golden"] = {
            "status": "pass" if indexed else "fail",
            "detail": index_detail,
        }
        if indexed:
            ok, detail = run_validate_labels()
            rows["validate_labels_retry"] = {
                "status": "pass" if ok else "fail",
                "detail": detail,
            }

    if not ok:
        rows["eval_retrieval"] = {
            "status": "fail",
            "detail": "skipped — golden labels not present in Qdrant after index attempt",
        }
        return rows

    status, detail = run_quality_eval(
        compare=compare,
        threshold=threshold,
        rerank=rerank,
        mcp_url=mcp_url,
    )
    rows["eval_retrieval"] = {"status": status, "detail": detail}
    return rows


def quality_validation_passed(rows: dict[str, dict[str, str]]) -> bool:
    eval_row = rows.get("eval_retrieval", {})
    return eval_row.get("status") == "pass"


def _neo4j_auth_and_write_ok() -> tuple[bool, str]:
    proc = _run(
        [
            "docker",
            "exec",
            NEO4J_CONTAINER,
            "cypher-shell",
            "-u",
            "neo4j",
            "-p",
            GRAPH_INTEGRATION_PASSWORD,
            "CREATE (n:__GraphSmoke {k: 1}) DELETE n RETURN 1 AS ok",
        ]
    )
    detail = ((proc.stdout or "") + (proc.stderr or "")).strip()
    if proc.returncode != 0:
        return False, detail[-2000:] or "cypher-shell auth/write failed"
    if "ok" not in detail.lower() and "1" not in detail:
        return False, detail[-2000:] or "cypher-shell returned unexpected output"
    return True, "neo4j auth+write ok"


def run_graph_validation(*, external_tei: bool = False) -> tuple[str, str]:
    ok, detail = wait_http(NEO4J_URL)
    if not ok:
        return "fail", f"neo4j not healthy: {detail}"

    ok, auth_detail = _neo4j_auth_and_write_ok()
    if not ok:
        return "fail", f"neo4j auth/write failed: {auth_detail}"

    logs = _run(
        aspire_compose_cmd(
            "logs",
            "--no-color",
            "--tail",
            "500",
            "mcp",
            graph=True,
            gpu_colbert=get_accelerator() == "gpu",
            external_tei=external_tei,
        )
    )
    out = (logs.stdout or "") + (logs.stderr or "")
    if "graph_enabled" not in out:
        return "fail", "mcp did not log graph_enabled at startup"
    if "graph_disabled" in out:
        return "fail", "mcp logged graph_disabled (graph env not applied)"
    return (
        "pass",
        "neo4j healthy; auth+write ok; mcp registered expand_search_context (graph_enabled)",
    )


