def run_dotnet_smoke() -> tuple[bool, str]:
    try:
        proc = _run(["dotnet", "test", "CodebaseIndexer.slnx", "--nologo"])
    except FileNotFoundError:
        proc = None
        detail = "dotnet executable not found on host"
    else:
        if proc.returncode == 0:
            return True, "dotnet test passed (host)"
        detail = proc.stderr or proc.stdout or "dotnet test failed"
        if not _dotnet_sdk_resolution_failed(detail):
            return False, detail[-4000:]

    container = _run_dotnet_test_in_sdk_container()
    if container.returncode != 0:
        cdetail = container.stderr or container.stdout or "dotnet test failed in SDK container"
        first = detail.splitlines()[0] if detail else "unknown"
        return False, (
            f"host SDK unavailable ({first}); "
            f"container fallback failed:\n{cdetail[-3500:]}"
        )
    return True, f"dotnet test passed (container {DOTNET_SDK_IMAGE})"


def http_ok(url: str, timeout: float = 3.0) -> tuple[bool, str]:
    try:
        with urllib.request.urlopen(url, timeout=timeout) as resp:
            body = resp.read(500).decode("utf-8", errors="replace")
            return True, body[:200]
    except urllib.error.HTTPError as exc:
        return False, f"HTTP {exc.code}"
    except Exception as exc:
        return False, str(exc)


def wait_http(url: str, *, attempts: int = 30, interval: float = 2.0) -> tuple[bool, str]:
    last = ""
    for _ in range(attempts):
        ok, msg = http_ok(url)
        if ok:
            return True, msg
        last = msg
        time.sleep(interval)
    return False, last


def preflight_host_tei(*, tei_url: str = HOST_TEI_URL) -> tuple[bool, str]:
    ok, detail = http_ok(f"{tei_url.rstrip('/')}/health")
    if not ok:
        return False, f"host TEI not healthy at {tei_url}: {detail}"
    return True, detail


def check_tei_container_absent(
    *,
    tei_container: str = TEI_CONTAINER,
    run_cmd: Callable[..., subprocess.CompletedProcess] | None = None,
) -> tuple[bool, str]:
    runner = run_cmd or _run
    proc = runner(
        ["docker", "ps", "--filter", f"name={tei_container}", "--format", "{{.Names}}"]
    )
    if proc.returncode != 0:
        detail = (proc.stderr or proc.stdout or "docker ps failed").strip()
        return False, detail[-2000:]
    names = (proc.stdout or "").strip()
    if names:
        return False, f"container present: {names}"
    return True, "no bundled tei container"


def tei_embed_smoke(
    *,
    tei_url: str = TEI_URL,
    model: str = JINA_DENSE_MODEL,
    vector_size: int = DENSE_VECTOR_SIZE,
) -> tuple[bool, str]:
    payload = json.dumps({"model": model, "input": "gpu integration probe"}).encode()
    req = urllib.request.Request(
        f"{tei_url.rstrip('/')}/v1/embeddings",
        data=payload,
        headers={"Content-Type": "application/json"},
        method="POST",
    )
    try:
        with urllib.request.urlopen(req, timeout=120) as resp:
            body = json.loads(resp.read().decode("utf-8"))
            items = body.get("data") or []
            if not items:
                return False, "no embedding data"
            dim = len(items[0].get("embedding") or [])
            if dim != vector_size:
                return False, f"dimension {dim}, expected {vector_size}"
            return True, f"dim={dim}"
    except urllib.error.HTTPError as exc:
        err_body = exc.read(500).decode("utf-8", errors="replace")
        return False, f"HTTP {exc.code}: {err_body}"
    except Exception as exc:
        return False, str(exc)


