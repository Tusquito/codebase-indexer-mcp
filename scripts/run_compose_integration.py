def run_quality_eval(
    *,
    compare: Path,
    threshold: float,
    rerank: bool,
    mcp_url: str,
) -> tuple[str, str]:
    cmd = [
        "uv",
        "run",
        "python",
        "-m",
        "benchmarks.eval_retrieval",
        "--mcp-url",
        mcp_url,
        "--compare",
        str(compare),
        "--threshold",
        str(threshold),
        "--output",
        str(BENCHMARKS / ".eval-integration-results.json"),
    ]
    if rerank:
        cmd.append("--rerank")
    proc = _run(cmd, cwd=BENCHMARKS, env=_host_test_env())
    out = (proc.stdout or "") + (proc.stderr or "")
    if "SKIP:" in out:
        return "skip", out.strip()[-2000:]
    if proc.returncode == 0:
        return "pass", out.strip()[-2000:]
    return "fail", out.strip()[-2000:]


